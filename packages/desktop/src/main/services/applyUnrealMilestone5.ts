/**
 * Apply Milestone 5 to the active Unreal project by generating structured
 * progression/save-system artifacts into <projectRoot>/TriForge/.
 */
export async function applyUnrealMilestone5(
  projectPath: string,
  projectName: string,
  scaffold: UnrealScaffoldResult,
  milestone: UnrealMilestoneResult,
  m1ManifestExists: boolean,
  m2ManifestExists: boolean,
  m3ManifestExists: boolean,
  m4ManifestExists: boolean,
): Promise<M5ApplicationResult> {
  const projectRoot = path.dirname(projectPath);
  const triforgeDir = path.join(projectRoot, 'TriForge');
  const generatedAt = new Date().toISOString();
  const appliedFiles: M5AppliedFile[] = [];
  const warnings: string[]            = [];
  const errors: string[]              = [];

  if (!m1ManifestExists) {
    warnings.push(
      'M1_Manifest.json not found — Milestone 1 (Foundation) may not be applied. ' +
      'Create BP_PlayerCharacter and BP_GameMode in Unreal Editor before wiring save/load (see M5_SaveGameConfig.json).',
    );
  }
  if (!m2ManifestExists) {
    warnings.push(
      'M2_Manifest.json not found — Milestone 2 (Primary Loop / HUD) may not be applied. ' +
      'Health restore on respawn and OnLevelUp HUD binding require M2 components.',
    );
  }
  if (!m3ManifestExists) {
    warnings.push(
      'M3_Manifest.json not found — Milestone 3 (Supporting Systems) may not be applied. ' +
      'Inventory save/restore field is included but has no source until M3 is wired.',
    );
  }
  if (!m4ManifestExists) {
    warnings.push(
      'M4_Manifest.json not found — Milestone 4 (Enemy / Combat) may not be applied. ' +
      'Kill count and score save fields are included but have no source until M4 is wired.',
    );
  }

  // ── Ensure TriForge directory exists ─────────────────────────────────────
  try {
    fs.mkdirSync(triforgeDir, { recursive: true });
  } catch (e: unknown) {
    const msg = e instanceof Error ? e.message : String(e);
    return {
      ok: false, projectRoot, triforgeDir,
      appliedFiles: [], warnings,
      errors: [`Failed to create TriForge directory at ${triforgeDir}: ${msg}`],
    };
  }

  // ── Write helper ──────────────────────────────────────────────────────────
  function writeFile(filename: string, content: string, description: string): boolean {
    const absolutePath = path.join(triforgeDir, filename);
    const relativePath = path.join('TriForge', filename);
    try {
      fs.writeFileSync(absolutePath, content, 'utf-8');
      appliedFiles.push({ absolutePath, relativePath, description });
      return true;
    } catch (e: unknown) {
      const msg = e instanceof Error ? e.message : String(e);
      warnings.push(`Could not write ${relativePath}: ${msg}`);
      return false;
    }
  }

  // ── Write M5 artifacts ────────────────────────────────────────────────────
  writeFile(
    'M5_Progression_Setup.md',
    buildSetupMarkdown(
      projectName, scaffold, milestone, generatedAt,
      m1ManifestExists, m2ManifestExists, m3ManifestExists, m4ManifestExists,
    ),
    'Milestone 5 implementation guide — save game, checkpoint system, progression/XP, and M1–M4 integration.',
  );

  writeFile(
    'M5_SaveGameConfig.json',
    buildSaveGameConfigJson(projectName, scaffold, generatedAt),
    'Save game spec — field list, source components, save/restore triggers (scaffold-adaptive).',
  );

  writeFile(
    'M5_ProgressionConfig.json',
    buildProgressionConfigJson(projectName, scaffold, generatedAt),
    'Progression/XP config — level thresholds, XP sources, and unlock specs (scaffold-adaptive).',
  );

  writeFile(
    'M5_CheckpointConfig.json',
    buildCheckpointConfigJson(projectName, scaffold, generatedAt),
    'Checkpoint config — trigger volume spec, respawn behavior, placement guidance, minimum requirements.',
  );

  // ── Manifest ──────────────────────────────────────────────────────────────
  const chainIntegrityNotes: string[] = [];
  if (!m1ManifestExists) chainIntegrityNotes.push('M1_Manifest.json missing.');
  if (!m2ManifestExists) chainIntegrityNotes.push('M2_Manifest.json missing — health restore on respawn requires M2.');
  if (!m3ManifestExists) chainIntegrityNotes.push('M3_Manifest.json missing — inventory restore requires M3.');
  if (!m4ManifestExists) chainIntegrityNotes.push('M4_Manifest.json missing — kill count / XP sources require M4.');

  const dependsOn: string[] = [];
  if (m4ManifestExists) dependsOn.push('M4_Manifest.json');
  else if (m3ManifestExists) dependsOn.push('M3_Manifest.json');
  else if (m2ManifestExists) dependsOn.push('M2_Manifest.json');
  else if (m1ManifestExists) dependsOn.push('M1_Manifest.json');

  const manifest = {
    triforgeVersion: '1.0',
    milestone:       'M5',
    project:         projectName,
    projectPath,
    generatedAt,
    packId:          'pack.unreal-m5-execute',
    dependsOn,
    files: appliedFiles.map(f => ({ relativePath: f.relativePath, description: f.description })),
    notes: [
      `Save fields: health always; survival stats ${isSurvivalGoal(scaffold) ? 'included' : 'excluded'}; inventory ${hasInventoryItems(scaffold) ? 'included' : 'excluded'}; kills/score ${hasEnemyItems(scaffold) ? 'included' : 'excluded'}`,
      'Save trigger: checkpoint activation (primary) + intentional quit (secondary)',
      'Progression: 10 levels; XP sources adapt to scaffold (kills, checkpoints, resources)',
      'Checkpoint: minimum 2 required for prototype (Start + MidPoint)',
      ...chainIntegrityNotes,
    ],
    recommendedNext: 'pack.unreal-editor-operate',
  };

  writeFile(
    'M5_Manifest.json',
    JSON.stringify(manifest, null, 2),
    'Machine-readable manifest of all M5 generated files for future pack chaining.',
  );

  // Companion Python script — run from Unreal Editor → Tools → Execute Python
  // Script to materialize the M5 save/progression scaffolds. Idempotent.
  writeFile(
    'M5_Apply.py',
    buildMilestone5PythonScript(projectName, scaffold, milestone, generatedAt),
    'Companion Python script — runs inside Unreal Editor to create the SaveGame, ' +
    'checkpoint, and progression component scaffolds described above.',
  );

  // ── B5: Post-write validation ──────────────────────────────────────────
  const validation = validateMilestoneBatch(
    appliedFiles.map(f => ({
      absolutePath:      f.absolutePath,
      relativePath:      f.relativePath,
      expectedMilestone: 'M5',
      isManifest:        f.relativePath.endsWith('M5_Manifest.json'),
    })),
  );
  if (validation.failed.length > 0) {
    warnings.push(...validation.warnings);
    const failedPaths = new Set(validation.failed.map(f => f.absolutePath));
    const survivingFiles = appliedFiles.filter(f => !failedPaths.has(f.absolutePath));
    appliedFiles.length = 0;
    appliedFiles.push(...survivingFiles);
  }

  const ok = appliedFiles.length > 0 && errors.length === 0;
  return { ok, projectRoot, triforgeDir, appliedFiles, warnings, errors };
}
