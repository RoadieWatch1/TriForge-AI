function buildSetupMarkdown(
  projectName: string,
  scaffold: UnrealScaffoldResult,
  milestone: UnrealMilestoneResult,
  generatedAt: string,
  m1Present: boolean,
  m2Present: boolean,
  m3Present: boolean,
  m4Present: boolean,
): string {
  const m6     = milestone.milestones.find(m => m.id === 'M6');
  const steps  = m6?.steps ?? [];
  const surv   = isSurvivalGoal(scaffold);
  const inv    = hasInventoryItems(scaffold);
  const enemy  = hasEnemyItems(scaffold);

  const stepsMarkdown = steps.length > 0
    ? steps.map((s, i) => `### ${i + 1}. ${s.name}\n${s.description}\n`).join('\n')
    : '> Progression/save implementation steps derived from prototype goal.';

  const chainWarnings: string[] = [];
  if (!m1Present) chainWarnings.push('⚠ M1_Manifest.json not found — M1 (Foundation) may not be applied.');
  if (!m2Present) chainWarnings.push('⚠ M2_Manifest.json not found — M2 (Primary Loop / HUD) may not be applied.');
  if (!m3Present) chainWarnings.push('⚠ M3_Manifest.json not found — M3 (Supporting Systems) may not be applied.');
  if (!m4Present) chainWarnings.push('⚠ M4_Manifest.json not found — M4 (Enemy / Combat) may not be applied. Save data will include kill/score fields but they have no source until M4 is wired.');

  return `# TriForge — Milestone 5: Progression / Save Systems

**Project:** ${projectName}
**Prototype Goal:** ${scaffold.prototypeGoal}
**Survival Mode:** ${surv ? 'Yes' : 'No'}
**Inventory System:** ${inv ? 'Yes' : 'No'}
**Enemy/Combat Present:** ${enemy ? 'Yes' : 'No'}
**Generated:** ${generatedAt}
**Generated by:** TriForge pack.unreal-m5-execute (Phase 4, Step 6)
**Depends on:** M1 (Foundation), M2 (Primary Loop), M3 (Supporting Systems), M4 (Enemy / Combat)

${chainWarnings.length > 0 ? `---\n\n## Chain Integrity Warnings\n\n${chainWarnings.join('\n')}\n` : ''}
---

## Milestone Goal

${m6?.goal ?? 'Implement save-game persistence and a progression loop so the prototype remembers player state across sessions.'}

---

## Implementation Steps

${stepsMarkdown}

---

## Save Game Object (BP_SaveGame)

Create a Blueprint class named **BP_SaveGame** in \`Content/SaveData/\` with
**parent class: SaveGame** (search "Save Game" in the Blueprint picker).

See \`M5_SaveGameConfig.json\` for the full field spec.

### Required variables on BP_SaveGame

| Variable              | Type    | Default | Notes                                          |
|---------------------- |-------- |-------- |----------------------------------------------- |
| PlayerHealth          | Float   | 100.0   | Saved from BP_HealthComponent.CurrentHealth    |
${surv ? `| PlayerHunger          | Float   | 100.0   | Saved from BP_SurvivalStatsComponent           |
| PlayerThirst          | Float   | 100.0   | Saved from BP_SurvivalStatsComponent           |
` : ''}\
${inv ? `| InventoryItems        | Array   | []      | Serialized from BP_InventoryComponent          |
` : ''}\
${enemy ? `| TotalKills            | Integer | 0       | Incremented on enemy death                     |
| CurrentScore          | Integer | 0       | Updated per kill / objective                   |
` : ''}\
| LastCheckpointTag     | Name    | "Start" | FName tag of the last activated checkpoint     |
| TotalPlaytimeSec      | Float   | 0.0     | Accumulated play time in seconds               |

### Saving

\`\`\`
UGameplayStatics::SaveGameToSlot(BP_SaveGame_Instance, "Slot1", 0)
\`\`\`

Call this:
- When the player activates a checkpoint
- On intentional quit (from pause menu)
- Do NOT auto-save on every tick

### Loading

\`\`\`
Cast<BP_SaveGame>(UGameplayStatics::LoadGameFromSlot("Slot1", 0))
\`\`\`

Call this in **BP_GameMode BeginPlay**. If the slot does not exist, create a fresh
\`BP_SaveGame\` instance with defaults.

---

## Checkpoint System (BP_Checkpoint)

See \`M5_CheckpointConfig.json\` for placement and respawn parameters.

Create **BP_Checkpoint** (Actor Blueprint) in \`Content/Gameplay/\`:

1. Add a **Box Collision** component as the trigger volume.
2. On **ActorBeginOverlap** (PlayerCharacter only):
   - Set \`LastCheckpointTag\` on the active save game instance.
   - Call \`SaveGameToSlot\` to persist immediately.
   - Play a brief feedback effect (particle/sound).
3. On player death (from M2/M4 health system):
   - Load the save slot.
   - Teleport the player to the checkpoint actor tagged \`LastCheckpointTag\`.
   - Restore health/stats to saved values.

---

## Progression / XP (BP_ProgressionComponent)

See \`M5_ProgressionConfig.json\` for XP thresholds and unlock definitions.

Create **BP_ProgressionComponent** (Actor Component) in \`Content/Components/\`
and add it to **BP_PlayerCharacter**.

### How XP is awarded

${enemy ? `- On enemy death: add XP based on archetype threat level (see M4_EnemyArchetypes.json)\n` : ''}\
- On checkpoint activation: small XP bonus
${surv ? `- On resource collection: small XP bonus per rare resource\n` : ''}\

### Level-up flow

\`\`\`
AddXP(amount) → check if XP ≥ XPThresholds[CurrentLevel] → if yes, LevelUp()
LevelUp() → increment CurrentLevel, fire OnLevelUp delegate, check unlock table
\`\`\`

Fire **OnLevelUp** (Event Dispatcher) and bind it in WBP_HUD (M2) to show a
level-up notification overlay.

---

## Integration with Prior Milestones

| Milestone | Integration Point                                           |
|---------- |------------------------------------------------------------ |
| M2        | On death, restore health from save. OnLevelUp binds to HUD.|
| M3        | On load, restore inventory from \`InventoryItems\` save field.|
| M4        | On enemy death, award XP + increment kill count in save.   |

---

## Notes & Assumptions

- Use a single save slot ("Slot1") for the prototype — multi-slot save is a polish step.
- Do NOT save Blueprint object references — save plain data (float, int, string, name).
- Checkpoint tags must match exactly — use FName constants, not magic strings.
- If \`LoadGameFromSlot\` returns null, always fall back to fresh defaults (never crash).
- Progression unlocks in \`M5_ProgressionConfig.json\` are fully spec-defined — wire each to its Blueprint variable during implementation.

---

## Next Steps After M5

1. Create BP_SaveGame with all required variables.
2. Create BP_GameMode and implement save/load in BeginPlay and quit flow.
3. Create BP_Checkpoint and place at least one in the test level.
4. Create BP_ProgressionComponent and add to BP_PlayerCharacter.
${enemy ? '5. Wire enemy death → AddXP + TotalKills increment.\n' : ''}\
${!enemy ? '5' : '6'}. Verify save persists across Play-in-Editor sessions (stop and re-play).
${!enemy ? '6' : '7'}. Run \`pack.unreal-build\` to verify the full M1–M5 chain compiles cleanly.

---

*This file was generated by TriForge and can be safely edited.*
`;
}

