/**
 * Apply Milestone 4 to the active Unreal project by generating structured
 * enemy/combat artifacts into <projectRoot>/TriForge/.
 *
 * @param projectPath      Confirmed .uproject path (high-confidence)
 * @param projectName      Project name
 * @param scaffold         Scaffold result from generateUnrealSystemScaffold()
 * @param milestone        Milestone result from generateUnrealMilestones()
 * @param m1ManifestExists Whether M1_Manifest.json is present (chain signal)
 * @param m2ManifestExists Whether M2_Manifest.json is present (chain signal)
 * @param m3ManifestExists Whether M3_Manifest.json is present (chain signal)
 */
export async function applyUnrealMilestone4(
  projectPath: string,
  projectName: string,
  scaffold: UnrealScaffoldResult,
  milestone: UnrealMilestoneResult,
  m1ManifestExists: boolean,
  m2ManifestExists: boolean,
  m3ManifestExists: boolean,
): Promise<M4ApplicationResult> {
  const projectRoot = path.dirname(projectPath);
  const triforgeDir = path.join(projectRoot, 'TriForge');
  const generatedAt = new Date().toISOString();
  const appliedFiles: M4AppliedFile[] = [];
  const warnings: string[]            = [];
  const errors: string[]              = [];

  if (!m1ManifestExists) {
    warnings.push(
      'M1_Manifest.json was not found in TriForge/ — Milestone 1 (Foundation) may not have been applied yet. ' +
      'M4 artifacts are still written, but review the M1 foundation before implementing M4.',
    );
  }
  if (!m2ManifestExists) {
    warnings.push(
      'M2_Manifest.json was not found in TriForge/ — Milestone 2 (Primary Loop / HUD) may not have been applied yet. ' +
      'M4 enemy attacks will not integrate with the HUD health display until M2 is applied.',
    );
  }
  if (!m3ManifestExists) {
    warnings.push(
      'M3_Manifest.json was not found in TriForge/ — Milestone 3 (Supporting Systems) may not have been applied yet. ' +
      'M4 loot hints reference M3 item types — apply M3 before wiring enemy loot drops.',
    );
  }

  // ── Ensure TriForge directory exists ─────────────────────────────────────
  try {
    fs.mkdirSync(triforgeDir, { recursive: true });
  } catch (e: unknown) {
    const msg = e instanceof Error ? e.message : String(e);
    return {
      ok: false, projectRoot, triforgeDir,
      appliedFiles: [], warnings,
      errors: [`Failed to create TriForge directory at ${triforgeDir}: ${msg}`],
    };
  }

  // ── Write helper ──────────────────────────────────────────────────────────
  function writeFile(filename: string, content: string, description: string): boolean {
    const absolutePath = path.join(triforgeDir, filename);
    const relativePath = path.join('TriForge', filename);
    try {
      fs.writeFileSync(absolutePath, content, 'utf-8');
      appliedFiles.push({ absolutePath, relativePath, description });
      return true;
    } catch (e: unknown) {
      const msg = e instanceof Error ? e.message : String(e);
      warnings.push(`Could not write ${relativePath}: ${msg}`);
      return false;
    }
  }

  // ── Write M4 artifacts ────────────────────────────────────────────────────
  writeFile(
    'M4_EnemyCombat_Setup.md',
    buildSetupMarkdown(projectName, scaffold, milestone, generatedAt, m1ManifestExists, m2ManifestExists, m3ManifestExists),
    'Milestone 4 implementation guide — enemy base class, archetypes, detection/aggro, and M2 damage integration.',
  );

  writeFile(
    'M4_EnemyArchetypes.json',
    buildEnemyArchetypesJson(projectName, scaffold, generatedAt),
    'Enemy archetype definitions — 3 archetypes adapted to prototype goal (survival/sci-fi/horror/default).',
  );

  writeFile(
    'M4_CombatConfig.json',
    buildCombatConfigJson(projectName, scaffold, generatedAt),
    'Foundational combat tuning config — player/enemy damage, health scaling, weapon categories.',
  );

  writeFile(
    'M4_DetectionAggroConfig.json',
    buildDetectionAggroConfigJson(projectName, scaffold, generatedAt),
    'Detection and aggro config — sight/hearing ranges, chase radius, and aggro state machine spec ready to wire.',
  );

  // ── Manifest ──────────────────────────────────────────────────────────────
  const chainIntegrityNotes: string[] = [];
  if (!m1ManifestExists) chainIntegrityNotes.push('M1_Manifest.json missing — M1 foundation may not be applied.');
  if (!m2ManifestExists) chainIntegrityNotes.push('M2_Manifest.json missing — M2 primary loop / HUD may not be applied.');
  if (!m3ManifestExists) chainIntegrityNotes.push('M3_Manifest.json missing — M3 supporting systems may not be applied.');

  const dependsOn: string[] = [];
  if (m3ManifestExists) dependsOn.push('M3_Manifest.json');
  if (m2ManifestExists && !m3ManifestExists) dependsOn.push('M2_Manifest.json');
  if (m1ManifestExists && !m2ManifestExists && !m3ManifestExists) dependsOn.push('M1_Manifest.json');

  const theme = detectCombatTheme(scaffold);
  const manifest = {
    triforgeVersion: '1.0',
    milestone:       'M4',
    project:         projectName,
    projectPath,
    generatedAt,
    packId:          'pack.unreal-m4-execute',
    combatTheme:     theme,
    dependsOn,
    files: appliedFiles.map(f => ({ relativePath: f.relativePath, description: f.description })),
    notes: [
      `Combat theme: ${theme} (derived from prototype goal and scaffold signals)`,
      `Enemy archetypes: 3 defined (${theme === 'survival' ? 'Shambler / Runner / Brute' : theme === 'scifi' ? 'Patrol Drone / Raider / Armored Heavy' : theme === 'horror' ? 'Stalker / Brute / Swarm Unit' : 'Melee Grunt / Ranged Threat / Heavy Tank'})`,
      'M2 integration: enemy attacks route through UGameplayStatics::ApplyDamage → BP_HealthComponent',
      'M3 integration: enemy loot hints reference M3 item types (apply M3 first for full wiring)',
      ...chainIntegrityNotes,
    ],
    recommendedNext: 'pack.unreal-m5-execute',
  };

  writeFile(
    'M4_Manifest.json',
    JSON.stringify(manifest, null, 2),
    'Machine-readable manifest of all M4 generated files for future pack chaining.',
  );

  // Companion Python script — run from Unreal Editor → Tools → Execute Python
  // Script to materialize the M4 enemy/combat scaffolds. Idempotent.
  writeFile(
    'M4_Apply.py',
    buildMilestone4PythonScript(projectName, scaffold, milestone, generatedAt),
    'Companion Python script — runs inside Unreal Editor to create the enemy, ' +
    'AI controller, and damage component scaffolds described above.',
  );

  // ── B5: Post-write validation ──────────────────────────────────────────
  const validation = validateMilestoneBatch(
    appliedFiles.map(f => ({
      absolutePath:      f.absolutePath,
      relativePath:      f.relativePath,
      expectedMilestone: 'M4',
      isManifest:        f.relativePath.endsWith('M4_Manifest.json'),
    })),
  );
  if (validation.failed.length > 0) {
    warnings.push(...validation.warnings);
    const failedPaths = new Set(validation.failed.map(f => f.absolutePath));
    const survivingFiles = appliedFiles.filter(f => !failedPaths.has(f.absolutePath));
    appliedFiles.length = 0;
    appliedFiles.push(...survivingFiles);
  }

  const ok = appliedFiles.length > 0 && errors.length === 0;
  return { ok, projectRoot, triforgeDir, appliedFiles, warnings, errors };
}
