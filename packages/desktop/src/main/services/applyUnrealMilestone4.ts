// ── Scaffold signal helpers ───────────────────────────────────────────────────

function hasEnemyItems(scaffold: UnrealScaffoldResult): boolean {
  return scaffold.scaffoldItems.some(i => i.category === 'enemy');
}

function hasSurvivalItems(scaffold: UnrealScaffoldResult): boolean {
  return scaffold.scaffoldItems.some(i => i.category === 'survival');
}

function isSurvivalGoal(scaffold: UnrealScaffoldResult): boolean {
  const lower = scaffold.prototypeGoal.toLowerCase();
  return /survival|survive|hunger|thirst|zombie|infected|creature/.test(lower) || hasSurvivalItems(scaffold);
}

function isSciFiGoal(scaffold: UnrealScaffoldResult): boolean {
  const lower = scaffold.prototypeGoal.toLowerCase();
  return /sci.fi|scifi|space|robot|drone|mech|futur|cyber|alien/.test(lower);
}

function isHorrorGoal(scaffold: UnrealScaffoldResult): boolean {
  const lower = scaffold.prototypeGoal.toLowerCase();
  return /horror|stalker|monster|brute|terror|haunt|dark/.test(lower);
}

function detectCombatTheme(scaffold: UnrealScaffoldResult): 'survival' | 'scifi' | 'horror' | 'default' {
  if (isSurvivalGoal(scaffold)) return 'survival';
  if (isSciFiGoal(scaffold))    return 'scifi';
  if (isHorrorGoal(scaffold))   return 'horror';
  return 'default';
}

