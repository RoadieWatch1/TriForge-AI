// ── applyUnrealMilestone3.ts — Milestone 3 Project File Generator ─────────────
//
// Phase 4, Step 4B: Third real project modification helper.
//
// Writes Milestone 3 implementation artifacts into <projectRoot>/TriForge/.
// Covers the supporting-systems layer: inventory component, item data definitions,
// and interaction setup. All content adapts to scaffold signals (survival/enemy/
// inventory) detected from the prototype goal and scaffold categories.
//
// TRULY IMPLEMENTED:
//   - M3_SupportingSystems_Setup.md  — human-readable M3 implementation guide
//   - M3_InventoryConfig.json        — inventory component spec (slots/weight/categories)
//   - M3_ItemData.json               — item data definitions (scaffold-adaptive)
//   - M3_InteractionConfig.json      — interaction system config (trace/prompt/component)
//   - M3_Manifest.json               — machine-readable manifest with M2 dependency link
//
// NOT YET:
//   - Blueprint component generation
//   - Editor-native asset mutation
//   - Post-write editor reload

import fs   from 'fs';
import path from 'path';
import type { UnrealScaffoldResult }  from '@triforge/engine';
import type { UnrealMilestoneResult } from '@triforge/engine';

// ── Output types ──────────────────────────────────────────────────────────────

export interface M3AppliedFile {
  absolutePath: string;
  relativePath: string;
  description: string;
}

export interface M3ApplicationResult {
  ok: boolean;
  projectRoot: string;
  triforgeDir: string;
  appliedFiles: M3AppliedFile[];
  warnings: string[];
  errors: string[];
}

// ── Scaffold signal helpers ───────────────────────────────────────────────────

function hasSurvivalItems(scaffold: UnrealScaffoldResult): boolean {
  return scaffold.scaffoldItems.some(i => i.category === 'survival');
}

function hasInventoryItems(scaffold: UnrealScaffoldResult): boolean {
  return scaffold.scaffoldItems.some(i => i.category === 'inventory');
}

function hasEnemyItems(scaffold: UnrealScaffoldResult): boolean {
  return scaffold.scaffoldItems.some(i => i.category === 'enemy');
}

function isSurvivalGoal(scaffold: UnrealScaffoldResult): boolean {
  const lower = scaffold.prototypeGoal.toLowerCase();
  return /survival|survive|hunger|thirst|stamina/.test(lower) || hasSurvivalItems(scaffold);
}

// ── File content builders ─────────────────────────────────────────────────────

function buildSetupMarkdown(
  projectName: string,
  scaffold: UnrealScaffoldResult,
  milestone: UnrealMilestoneResult,
  generatedAt: string,
  m1Present: boolean,
  m2Present: boolean,
): string {
  const m4     = milestone.milestones.find(m => m.id === 'M4');
  const steps  = m4?.steps ?? [];
  const surv   = isSurvivalGoal(scaffold);
  const inv    = hasInventoryItems(scaffold);
  const enemy  = hasEnemyItems(scaffold);

  const stepsMarkdown = steps.length > 0
    ? steps.map((s, i) => `### ${i + 1}. ${s.name}\n${s.description}\n`).join('\n')
    : '> Supporting-systems steps derived from prototype goal.';

  const chainWarnings: string[] = [];
  if (!m1Present) chainWarnings.push('⚠ M1_Manifest.json not found — ensure Milestone 1 (Foundation) was applied before implementing M3.');
  if (!m2Present) chainWarnings.push('⚠ M2_Manifest.json not found — ensure Milestone 2 (Primary Loop / HUD) was applied before implementing M3.');

  return `# TriForge — Milestone 3: Supporting Systems

**Project:** ${projectName}
**Prototype Goal:** ${scaffold.prototypeGoal}
**Inventory System:** ${inv ? 'Yes (scaffold detected)' : 'Minimal (no explicit inventory scaffold)'}
**Survival Items:** ${surv ? 'Yes' : 'No'}
**Enemy/Combat Present:** ${enemy ? 'Yes' : 'No'}
**Generated:** ${generatedAt}
**Generated by:** TriForge pack.unreal-m3-execute (Phase 4, Step 4B)
**Depends on:** M1_Manifest.json (Foundation), M2_Manifest.json (Primary Loop)

${chainWarnings.length > 0 ? `---\n\n## Chain Integrity Warnings\n\n${chainWarnings.join('\n')}\n` : ''}
---

## Milestone Goal

${m4?.goal ?? 'Implement supporting systems: inventory, item data, and interaction so the prototype has a usable economy layer.'}

---

## Implementation Steps

${stepsMarkdown}

---

## Inventory Component (BP_InventoryComponent)

Create an Actor Component Blueprint named **BP_InventoryComponent** in \`Content/Components/\`.

See \`TriForge/M3_InventoryConfig.json\` for the full slot/category spec.

### Required variables

| Variable        | Type             | Default | Notes                                 |
|---------------- |----------------- |-------- |-------------------------------------- |
| MaxSlots        | Integer          | ${inv ? 20 : 10}       | Total inventory slots available        |
| MaxWeightKg     | Float            | ${inv ? 50.0 : 20.0}  | Weight limit; 0 = no weight limit      |
| Items           | Array (ItemData) | []      | Runtime array of held item structs     |

### Required functions

| Name               | Description                                           |
|------------------- |------------------------------------------------------ |
| AddItem(item)      | Add item if slot and weight allow; return bool success |
| RemoveItem(itemId) | Remove item by ID; return removed item or none        |
| HasItem(itemId)    | Check if inventory contains a given item ID           |
| GetWeight()        | Return current total weight of all held items         |

Add **BP_InventoryComponent** to **BP_PlayerCharacter** (from M1).

---

## Item Data Structure (BP_ItemData / DT_Items)

See \`TriForge/M3_ItemData.json\` for the full item definition list.

Recommended approach:
1. Create a **Data Table** (DT_Items) using a struct Row Type that matches the item schema in \`M3_ItemData.json\`.
2. Create a **BP_ItemData** struct Blueprint to define the row type.
3. Reference items in \`BP_InventoryComponent\` via row handles, not hard-coded classes.

---

## Interaction System (BP_InteractionComponent)

Create an Actor Component Blueprint named **BP_InteractionComponent** in \`Content/Components/\`.

See \`TriForge/M3_InteractionConfig.json\` for trace range, prompt text, and class defaults.

### How it works

1. On **Tick** (or on input), cast a line trace forward from the camera by \`TraceRange\` cm.
2. If the hit actor implements **\`BPI_Interactable\`** (an Interface), show the interaction prompt.
3. When the player presses the interact key (IA_Interact from M1), call \`BPI_Interactable::Interact()\` on the target.
4. Implement **\`BPI_Interactable\`** on any actor the player can pick up, open, or activate.

### Implementing item pickup

Any world item (e.g. **BP_WorldItem**) should:
- Implement \`BPI_Interactable\`
- In \`Interact()\`, call \`BP_InventoryComponent::AddItem()\` on the Player Character
- If add succeeds, destroy self; if not, show "Inventory Full" feedback

---

${surv ? `## Survival Item Notes

Since a survival prototype was detected, \`M3_ItemData.json\` includes food and water
consumable types. These items should:

- Restore the **Hunger** or **Thirst** stat (from M2 BP_SurvivalStatsComponent) when used
- Be usable from inventory via a Use action (wire to IA_Interact or a dedicated IA_UseItem)
- Have limited stack counts (e.g. 5 per slot) to create resource scarcity

---

` : ''}${enemy ? `## Loot / Ammo Notes

Since enemy/combat systems are planned (M5), \`M3_ItemData.json\` includes ammo item types.
Reserve inventory category space for ammo now so M5 integration is straightforward.

---

` : ''}## Notes & Assumptions

- BP_InventoryComponent must be added to BP_PlayerCharacter before item pickup works.
- BP_InteractionComponent should be added to BP_PlayerCharacter and driven by IA_Interact (from M1).
- BPI_Interactable should be a Blueprint Interface (not a C++ interface) for easy drag-and-drop implementation.
- Item IDs must be stable strings (e.g. "item.health_pack") — avoid int IDs that shift on reorder.
- Do NOT store item instances directly in saves — store item IDs + quantities for save compatibility.

---

## Next Steps After M3

1. Create DT_Items Data Table with rows matching \`M3_ItemData.json\`.
2. Create BP_InventoryComponent and add to BP_PlayerCharacter.
3. Create BPI_Interactable Blueprint Interface.
4. Create BP_InteractionComponent and add to BP_PlayerCharacter (trace + prompt logic).
5. Create a test world item (BP_WorldItem) that implements BPI_Interactable.
6. Verify item pickup adds to inventory and removes the world item.
7. Run \`pack.unreal-build\` to verify M1+M2+M3 systems compile cleanly.
8. When M3 is stable, proceed to Milestone 4 (${enemy ? 'Enemy / Combat' : 'Progression / Save'} Systems).

---

*This file was generated by TriForge and can be safely edited.*
`;
}

function buildInventoryConfigJson(
  projectName: string,
  scaffold: UnrealScaffoldResult,
  generatedAt: string,
): string {
  const inv   = hasInventoryItems(scaffold);
  const surv  = isSurvivalGoal(scaffold);
  const enemy = hasEnemyItems(scaffold);

  const categories: string[] = ['consumable', 'resource', 'tool'];
  if (inv)   categories.push('weapon', 'armor', 'key_item');
  if (surv)  categories.push('food', 'water');
  if (enemy) categories.push('ammo', 'grenade');

  return JSON.stringify({
    triforgeVersion: '1.0',
    milestone:       'M3',
    project:         projectName,
    prototypeGoal:   scaffold.prototypeGoal,
    generatedAt,
    instructions: [
      'Create BP_InventoryComponent (Actor Component) and add to BP_PlayerCharacter.',
      'Expose MaxSlots and MaxWeightKg as editable instance properties.',
      'Use a Data Table (DT_Items) for item definitions — do not hard-code item classes.',
      'Implement AddItem, RemoveItem, HasItem, GetWeight functions as described in M3_SupportingSystems_Setup.md.',
    ],
    componentBlueprint: {
      name:        'BP_InventoryComponent',
      parentClass: 'ActorComponent',
      contentPath: 'Content/Components/',
    },
    config: {
      maxSlots:    inv ? 20 : 10,
      maxWeightKg: inv ? 50.0 : 20.0,
      weightEnabled: true,
      stackingEnabled: true,
    },
    categories: categories.map(cat => ({
      id:    cat,
      label: cat.replace(/_/g, ' ').replace(/\b\w/g, c => c.toUpperCase()),
      included: true,
    })),
    notes: [
      `Slot count: ${inv ? '20 (inventory scaffold detected)' : '10 (minimal inventory)'}`,
      `Weight limit: ${inv ? '50 kg (inventory scaffold detected)' : '20 kg'}`,
      `Categories: ${categories.join(', ')}`,
    ],
  }, null, 2);
}

