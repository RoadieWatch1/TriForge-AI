// ── Main application function ─────────────────────────────────────────────────

/**
 * Apply Milestone 3 to the active Unreal project by generating structured
 * supporting-systems artifacts into <projectRoot>/TriForge/.
 *
 * @param projectPath      Confirmed .uproject path (high-confidence)
 * @param projectName      Project name
 * @param scaffold         Scaffold result from generateUnrealSystemScaffold()
 * @param milestone        Milestone result from generateUnrealMilestones()
 * @param m1ManifestExists Whether M1_Manifest.json is present (chain signal)
 * @param m2ManifestExists Whether M2_Manifest.json is present (chain signal)
 */
export async function applyUnrealMilestone3(
  projectPath: string,
  projectName: string,
  scaffold: UnrealScaffoldResult,
  milestone: UnrealMilestoneResult,
  m1ManifestExists: boolean,
  m2ManifestExists: boolean,
): Promise<M3ApplicationResult> {
  const projectRoot = path.dirname(projectPath);
  const triforgeDir = path.join(projectRoot, 'TriForge');
  const generatedAt = new Date().toISOString();
  const appliedFiles: M3AppliedFile[] = [];
  const warnings: string[]            = [];
  const errors: string[]              = [];

  if (!m1ManifestExists) {
    warnings.push(
      'M1_Manifest.json was not found in TriForge/ — Milestone 1 (Foundation) may not have been applied yet. ' +
      'M3 artifacts are still written, but review the M1 foundation before implementing M3.',
    );
  }

  if (!m2ManifestExists) {
    warnings.push(
      'M2_Manifest.json was not found in TriForge/ — Milestone 2 (Primary Loop / HUD) may not have been applied yet. ' +
      'M3 artifacts are still written, but review the M2 systems before implementing M3.',
    );
  }

  // ── Ensure TriForge directory exists ─────────────────────────────────────
  try {
    fs.mkdirSync(triforgeDir, { recursive: true });
  } catch (e: unknown) {
    const msg = e instanceof Error ? e.message : String(e);
    return {
      ok: false, projectRoot, triforgeDir,
      appliedFiles: [], warnings,
      errors: [`Failed to create TriForge directory at ${triforgeDir}: ${msg}`],
    };
  }

  // ── Write helper ──────────────────────────────────────────────────────────
  function writeFile(filename: string, content: string, description: string): boolean {
    const absolutePath = path.join(triforgeDir, filename);
    const relativePath = path.join('TriForge', filename);
    try {
      fs.writeFileSync(absolutePath, content, 'utf-8');
      appliedFiles.push({ absolutePath, relativePath, description });
      return true;
    } catch (e: unknown) {
      const msg = e instanceof Error ? e.message : String(e);
      warnings.push(`Could not write ${relativePath}: ${msg}`);
      return false;
    }
  }

  // ── Write M3 artifacts ────────────────────────────────────────────────────
  writeFile(
    'M3_SupportingSystems_Setup.md',
    buildSetupMarkdown(projectName, scaffold, milestone, generatedAt, m1ManifestExists, m2ManifestExists),
    'Milestone 3 implementation guide — inventory component, item data, and interaction system setup.',
  );

  writeFile(
    'M3_InventoryConfig.json',
    buildInventoryConfigJson(projectName, scaffold, generatedAt),
    'Inventory component spec — slot count, weight limit, and item categories (scaffold-adaptive).',
  );

  writeFile(
    'M3_ItemData.json',
    buildItemDataJson(projectName, scaffold, generatedAt),
    'Item data definitions — base items always present; survival/ammo items when scaffold signals detected.',
  );

  writeFile(
    'M3_InteractionConfig.json',
    buildInteractionConfigJson(projectName, scaffold, generatedAt),
    'Interaction system config — trace range, prompt text, BPI_Interactable interface, and pickup types.',
  );

  // ── Manifest ──────────────────────────────────────────────────────────────
  const chainIntegrityNotes: string[] = [];
  if (!m1ManifestExists) chainIntegrityNotes.push('M1_Manifest.json missing — M1 foundation may not be applied.');
  if (!m2ManifestExists) chainIntegrityNotes.push('M2_Manifest.json missing — M2 primary loop may not be applied.');

  const dependsOn: string[] = [];
  if (m2ManifestExists) dependsOn.push('M2_Manifest.json');
  if (m1ManifestExists && !m2ManifestExists) dependsOn.push('M1_Manifest.json');

  const manifest = {
    triforgeVersion: '1.0',
    milestone:       'M3',
    project:         projectName,
    projectPath,
    generatedAt,
    packId:          'pack.unreal-m3-execute',
    dependsOn,
    files: appliedFiles.map(f => ({ relativePath: f.relativePath, description: f.description })),
    notes: [
      `Inventory: ${hasInventoryItems(scaffold) ? 'full inventory scaffold (20 slots, 50 kg limit)' : 'minimal inventory (10 slots, 20 kg limit)'}`,
      `Item types: ${isSurvivalGoal(scaffold) ? 'includes survival consumables (food/water)' : 'base items only (no survival consumables)'}`,
      `Ammo items: ${hasEnemyItems(scaffold) ? 'included (enemy/combat scaffold detected)' : 'not included'}`,
      ...chainIntegrityNotes,
    ],
    recommendedNext: 'pack.unreal-m4-execute',
  };

  writeFile(
    'M3_Manifest.json',
    JSON.stringify(manifest, null, 2),
    'Machine-readable manifest of all M3 generated files for future pack chaining.',
  );

  const ok = appliedFiles.length > 0 && errors.length === 0;
  return { ok, projectRoot, triforgeDir, appliedFiles, warnings, errors };
}
